--  Creates a DEDICATED set of  expert_*  tables for the Expert Hardware site.
--
--  IMPORTANT: This does NOT touch your  capslock_*  tables (that is your
--  separate PC-components store living in the same Supabase project). The two
--  stores stay completely independent.
--
--  HOW TO RUN:
--    Supabase Dashboard  ->  SQL Editor  ->  New query  ->  paste this  ->  Run
